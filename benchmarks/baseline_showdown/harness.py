def rmse(pred: Sequence[float], actual: Sequence[float]) -> float:
    p = np.asarray(pred, dtype=np.float64)
    a = np.asarray(actual, dtype=np.float64)
    if p.shape != a.shape or p.size == 0:
        return float("nan")
    return float(np.sqrt(np.mean((p - a) ** 2)))


def relative_rmse(pred: Sequence[float], actual: Sequence[float]) -> float:
    base = rmse(pred, actual)
    scale = max(float(np.mean(np.asarray(actual, dtype=np.float64) ** 2)), 1e-12)
    return base / math.sqrt(scale)


def coeff_within_pct(expected: float, observed: float, pct: float = COEFF_MATCH_TOL) -> bool:
    if not math.isfinite(expected) or not math.isfinite(observed):
        return False
    denom = max(abs(expected), 1e-9)
    return abs(observed - expected) / denom <= pct


