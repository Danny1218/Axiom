@dataclass
class TaskResult:
    task_id: str
    family: str
    in_family: bool
    ground_truth: str
    contenders: List[ContenderResult]
    best_neural_extrap_rmse: Optional[float] = None
    axiom_extrap_margin_x: Optional[float] = None


class Timer:
    def __init__(self) -> None:
        self._t0 = 0.0
        self.elapsed_ms = 0.0

    def __enter__(self) -> Timer:
        self._t0 = time.perf_counter()
        return self

    def __exit__(self, *args: object) -> None:
        self.elapsed_ms = (time.perf_counter() - self._t0) * 1000.0


def rows_to_matrix(rows: Sequence[Mapping[str, float]], keys: Sequence[str]) -> np.ndarray:
    return np.array([[float(r[k]) for k in keys] for r in rows], dtype=np.float64)


def write_json(path: Path, payload: Mapping[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=False) + "\n", encoding="utf-8")


def _fmt(v: Optional[float]) -> str:
    if v is None or (isinstance(v, float) and not math.isfinite(v)):
        return "—"
    if v == 0.0:
        return "0"
    if abs(v) >= 1000 or (abs(v) > 0 and abs(v) < 1e-4):
        return f"{v:.3e}"
    return f"{v:.4f}"


def render_markdown(summary: Mapping[str, Any]) -> str:
    lines: List[str] = [
        "# Baseline showdown — symbolic extrapolation",
        "",
        f"Generated by `benchmarks/baseline_showdown/run_showdown.py` (seed={summary.get('seed')}).",
        "",
        "## Summary",
        "",
        summary.get("narrative", ""),
        "",
        "## Per-task results",
        "",
        "| Task | In-family | Axiom extrap | Best ML extrap | Axiom interp | Margin (×) | Axiom declined | Formula match |",
        "|------|-----------|--------------|----------------|--------------|------------|----------------|---------------|",
    ]
    for tr in summary.get("tasks", []):
        ax = next((c for c in tr["contenders"] if c["name"] == "axiom"), None)
        ml = [
            c["extrap_rmse"]
            for c in tr["contenders"]
            if c["name"] != "axiom" and c.get("extrap_rmse") is not None
        ]
        best_ml = min(ml) if ml else None
        margin = tr.get("axiom_extrap_margin_x")
        lines.append(
            "| {tid} | {inf} | {ax} | {ml} | {ip} | {mg} | {dec} | {fm} |".format(
                tid=tr["task_id"],
                inf="yes" if tr["in_family"] else "**sabotage**",
                ax=_fmt(ax.get("extrap_rmse") if ax else None),
                ml=_fmt(best_ml),
                ip=_fmt(ax.get("interp_rmse") if ax else None),
                mg=_fmt(margin),
                dec="yes" if ax and ax.get("declined") else "no",
                fm="yes" if ax and ax.get("formula_match") else ("no" if ax and ax.get("formula_match") is False else "—"),
            )
        )
    lines.extend(
        [
            "",
            "## Success criteria (honest)",
            "",
            summary.get("success_check", ""),
            "",
            "## Reproduce",
            "",
            "```powershell",
            "pip install -e \".[bench]\"",
            "python benchmarks/baseline_showdown/run_showdown.py",
            "```",
        ]
    )
    return "\n".join(lines) + "\n"


def build_summary(task_results: Sequence[TaskResult], *, wall_ms: float) -> Dict[str, Any]:
    in_family = [t for t in task_results if t.in_family]
    sabotage = [t for t in task_results if not t.in_family]
    wins = 0
    margin_ok = 0
    for tr in in_family:
        ax = next((c for c in tr.contenders if c.name == "axiom"), None)
        if ax is None or ax.declined or ax.extrap_rmse is None:
            continue
        ml = [
            c.extrap_rmse
            for c in tr.contenders
            if c.name != "axiom" and c.extrap_rmse is not None and math.isfinite(c.extrap_rmse)
        ]
        if not ml:
            continue
        best = min(ml)
        if ax.extrap_rmse < best:
            wins += 1
        if best > 0 and ax.extrap_rmse <= best / 10.0:
            margin_ok += 1
    sabotage_declined = sum(
        1
        for tr in sabotage
        for c in tr.contenders
        if c.name == "axiom" and c.declined
    )
    target_wins = 9
    target_margin = 10.0
    met = wins >= target_wins and sabotage_declined >= len(sabotage)
    narrative = (
        f"Axiom wins extrapolation RMSE on **{wins}/{len(in_family)}** in-family tasks "
        f"({margin_ok} by >={target_margin:.0f}x vs best neural baseline). "
        f"Sabotage tasks declined: **{sabotage_declined}/{len(sabotage)}**. "
        f"Total wall time: **{wall_ms / 1000:.1f}s**."
    )
    if not met:
        narrative += (
            " Success bar not fully met — see per-task table; results recorded honestly."
        )
    elif margin_ok < target_wins:
        narrative += (
            f" Margin bar ({margin_ok}/{len(in_family)} at >={target_margin:.0f}x) noted separately."
        )
    success_check = (
        f"- Extrapolation wins (in-family): {wins}/{len(in_family)} (bar >={target_wins})\n"
        f"- >={target_margin:.0f}x margin vs best ML: {margin_ok}/{len(in_family)} (bar >={target_wins})\n"
        f"- Sabotage declined: {sabotage_declined}/{len(sabotage)} (bar {len(sabotage)})"
    )
    return {
        "version": "1.4.0",
        "benchmark": "baseline_showdown",
        "seed": GLOBAL_SEED,
        "noise": f"{NOISE_FRAC * 100:.0f}% Gaussian per-row (sigma=0.03*|y|, unclipped)",
        "wall_ms": wall_ms,
        "success_bar_met": met,
        "narrative": narrative,
        "success_check": success_check,
        "tasks": [
            {
                "task_id": tr.task_id,
                "family": tr.family,
                "in_family": tr.in_family,
                "ground_truth": tr.ground_truth,
                "best_neural_extrap_rmse": tr.best_neural_extrap_rmse,
                "axiom_extrap_margin_x": tr.axiom_extrap_margin_x,
                "contenders": [asdict(c) for c in tr.contenders],
            }
            for tr in task_results
        ],
    }
