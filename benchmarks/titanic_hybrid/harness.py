"""Shared helpers for Titanic hybrid constraint audit."""

from __future__ import annotations

import json
import math
import random
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Any, Dict, List, Mapping, Optional, Sequence, Tuple

GLOBAL_SEED = 42
SPLIT_FRAC = 0.8
EPOCHS = 30
RULE_CAP = 0.15
FEATURES = ("Sex", "Pclass", "Fare", "Age")
RULE_NOTE = (
    "If Pclass > 2 (3rd class) AND Sex == 0 (male) AND Age > 17, survived_prob must be <= 0.15"
)


def rule_applies(row: Mapping[str, float]) -> bool:
    return (
        float(row.get("Pclass", 0.0)) > 2.0
        and float(row.get("Sex", 1.0)) < 0.5
        and float(row.get("Age", 0.0)) > 17.0
    )


def violates_rule(row: Mapping[str, float], prob: float) -> bool:
    return rule_applies(row) and float(prob) > RULE_CAP + 1e-6


def accuracy(rows: Sequence[Mapping[str, float]], probs: Sequence[float]) -> float:
    correct = 0
    for row, p in zip(rows, probs):
        pred = 1.0 if float(p) > 0.5 else 0.0
        if pred == float(row["Survived"]):
            correct += 1
    return correct / max(len(rows), 1)


def synthetic_edge_cases(n: int = 500, seed: int = 123) -> List[Dict[str, float]]:
    rng = random.Random(seed)
    rows: List[Dict[str, float]] = []
    for i in range(n):
        rows.append(
            {
                "PassengerId": float(i),
                "Sex": float(rng.choice([0.0, 1.0])),
                "Pclass": float(rng.choice([1.0, 2.0, 3.0, 3.0, 3.0])),
                "Fare": float(rng.uniform(0.0, 50_000.0)),
                "Age": float(rng.choice([rng.uniform(0, 90), 200.0, 999.0])),
                "Survived": 0.0,
            }
        )
    return rows


@dataclass
class ModelReport:
    name: str
    holdout_accuracy: float
    constraint_violations: int
    edge_cases_tested: int
    train_seconds: float
    note: Optional[str] = None


def write_json(path: Path, payload: Mapping[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")


def render_markdown(summary: Mapping[str, Any]) -> str:
    lines = [
        "# Titanic hybrid — accuracy vs constraint audit",
        "",
        f"Generated by `benchmarks/titanic_hybrid/run_hybrid_audit.py` (seed={summary.get('seed')}).",
        "",
        "## Rule under audit",
        "",
        summary.get("rule", RULE_NOTE),
        "",
        "## Holdout accuracy (80/20 split)",
        "",
        "| Model | Accuracy |",
        "|-------|----------|",
    ]
    for m in summary.get("models", []):
        lines.append(f"| {m['name']} | {m['holdout_accuracy']:.4f} |")
    lines.extend(
        [
            "",
            "## Constraint audit (500 synthetic edge cases)",
            "",
            "| Model | Violations |",
            "|-------|------------|",
        ]
    )
    for m in summary.get("models", []):
        lines.append(f"| {m['name']} | {m['constraint_violations']} |")
    lines.extend(
        [
            "",
            "## Narrative",
            "",
            summary.get("narrative", ""),
            "",
            "## Example explain trace",
            "",
            "```json",
            json.dumps(summary.get("explain_sample", {}), indent=2),
            "```",
            "",
            "## Reproduce",
            "",
            "```powershell",
            "pip install -e \".[bench]\"",
            "python benchmarks/titanic_hybrid/run_hybrid_audit.py",
            "```",
        ]
    )
    return "\n".join(lines) + "\n"
